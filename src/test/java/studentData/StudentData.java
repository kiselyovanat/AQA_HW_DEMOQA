package studentData;

public class StudentData {
    private String firstName,
            lastName,
            email,
            gender,
            userTelephoneNumber,
            fileName,
            address,
            state,
            city,
            subject,
            hobby,
            dayOfBirth,
            monthOfBirth,
            yearOfBirth;


    public String getFirstName() {
        return firstName;
    }

    public StudentData setFirstName(String firstName) {
        this.firstName = firstName;
        return this;
    }

    public String getLastName() {
        return lastName;
    }

    public StudentData setLastName(String lastName) {
        this.lastName = lastName;
        return this;
    }

    public String getEmail() {
        return email;
    }

    public StudentData setEmail(String email) {
        this.email = email;
        return this;
    }

    public String getGender() {
        return gender;
    }

    public StudentData setGender(String gender) {
        this.gender = gender;
        return this;
    }

    public String getUserTelephoneNumber() {
        return userTelephoneNumber;
    }

    public StudentData setUserTelephoneNumber(String userTelephoneNumber) {
        this.userTelephoneNumber = userTelephoneNumber;
        return this;
    }

    public String getFileName() {
        return fileName;
    }

    public StudentData setFileName(String fileName) {
        this.fileName = fileName;
        return this;
    }

    public String getAddress() {
        return address;
    }

    public StudentData setAddress(String address) {
        this.address = address;
        return this;
    }

    public String getState() {
        return state;
    }

    public StudentData setState(String state) {
        this.state = state;
        return this;
    }

    public String getCity() {
        return city;
    }

    public StudentData setCity(String city) {
        this.city = city;
        return this;
    }

    public String getSubject() {
        return subject;
    }

    public StudentData setSubject(String subject) {
        this.subject = subject;
        return this;
    }

    public String getHobby() {
        return hobby;
    }

    public StudentData setHobby(String hobby) {
        this.hobby = hobby;
        return this;
    }

    public String getDayOfBirth() {
        return dayOfBirth;
    }

    public StudentData setDayOfBirth(String dayOfBirth) {
        this.dayOfBirth = dayOfBirth;
        return this;
    }

    public String getMonthOfBirth() {
        return monthOfBirth;
    }

    public StudentData setMonthOfBirth(String monthOfBirth) {
        this.monthOfBirth = monthOfBirth;
        return this;
    }

    public String getYearOfBirth() {
        return yearOfBirth;
    }

    public StudentData setYearOfBirth(String yearOfBirth) {
        this.yearOfBirth = yearOfBirth;
        return this;
    }
}
